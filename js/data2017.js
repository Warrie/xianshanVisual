//求指定数组和、平均值
function array_sum(Array){
    var sum=0;
    for(var i=0;i<Array.length;i++){
        sum+=Array[i];
    }
    return sum;
}

function array_average(Array) {
    return (array_sum(Array)/Array.length).toFixed(2);
}

